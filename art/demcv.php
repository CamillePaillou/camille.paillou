                <p>Ma pratique est issue d’un dialogue entre toucher et mémoire, où le contact avec la matière ravive des réminiscences mentales et corporelles. 
                    Le papier mâché et le plastique interrogent la permanence et l’éphémère, la porosité et l’impénétrable. 
                    J’inscris mes œuvres dans un cycle de transformation et de disparition, à l’image d’une mémoire vivante et malléable.
                </p>
                <p>Le papier, support de traces et d’histoires, subit une métamorphose au contact du plastique, acquérant des textures multiples, entre rugosité et lissage, dureté apparente et fragilité réelle. 
                    Cette matière convoque nos sens, rappelant l’organique, le digestif, et s’inscrit dans une réflexion sur l’art abject : un travail autour du rejet, du déchet et de ce qui ne peut être intégré.
                </p>
                <p>Mes sculptures portent l’empreinte du geste et s’inscrivent dans une dynamique de répétition et de variation, révélant un processus en constante évolution. 
                    Le plastique cousu devient une enveloppe contrainte mais flexible, engageant un dialogue entre corps, sculpture et spectateur. 
                    J’interroge ainsi la porosité des frontières – intime/social, intérieur/extérieur et questionne la conception du corps comme matière vivante, non hermétique.
                </p>
                <p>À travers la sculpture et la performance, je travaille avec les souvenirs qui marquent la chair, avec les images et sensations qui persistent, s’effacent, reviennent. 
                    Mon corps et mon statut de femme sont au cœur de ma démarche, où s’entrelacent violence, sexualité, contrainte mais aussi plaisir, à travers des formes minimales et des manipulations performatives.
                </p>
